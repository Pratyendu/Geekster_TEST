let length; //variable declaration
let breadth;

length = 10; //variable initialization
breadth = 20;

// the step where a variable is defined but not assigned a value is called variable declaration.

// the step where variable is assigned a value is called variable initialization.

if (length === breadth) {
  console.log("It is a square");
} else {
  console.log("It is a rectangle");
}
